package com.example.alex.victorreader;

/**
 * Helper class to organize points in the two dimensional array of buttons.
 */
public class Coordinate {
    int x;
    int y;

    Coordinate() {
        x = 0;
        y = 0;
    }

    Coordinate(int newX, int newY) {
        x = newX;
        y = newY;
    }
}
